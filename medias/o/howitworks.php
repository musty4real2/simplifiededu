    <h2><a href="howitworks.php">How it Works</a></h2>
  <br/>
  <p style="padding-left:30px;"><a href="index.php">Simplified Education</a> is a simple philosophy behind learning. There is always a simple way to teach a complex subject. In order wards, a subject is as simple as the teacher or tutor who teaches it.</p>
 <p style="padding-left:30px;">Learning indeed can be fun if, a student can relate what is learnt to what he or she does everyday.</p>
 <p style="padding-left:30px;">It is important to note that the things we learn are stored differently, in our different types of memories. An understanding of these facts enables a student to engage all his/her different memory in active participation during a learning process.</p>
 <p style="padding-left:30px;">It has been scientifically proven that:<p>
<ul style="padding-left:30px;">
<li>We only remember about 10% of what we read (that is, textbook, notes, newspapers etc ). </li><br/>
<li>We remember as much as 21% of what we listen to (Lecture, Audio books, talk show or conversation etc)</li><br/>
<li>We remember about 31% of what we watch or see (Videos, Animation, Graphs and pictures).</li><br/>
<li>We remember about 51% of what we hear and see altogether (that is an experiment or a presentation or possibly an exhibition)</li><br/>
<li>Then when we participate in the learning in a way, we can remember as much as 70% of the material (that is, taking notes or jottings, asking question or discussion, thinking on what you have learnt)</li><br/>
<li>Finally when you teach, present or share what you know, you learn with others, you remember close to 90% or more of what you have learnt.</li><br/>
</ul>
